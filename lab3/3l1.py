# Ввод количества слов
N = int(input("Введите количество слов: "))

# Инициализация пустой строки
result = ""

# Цикл для ввода и объединения слов
for i in range(N):
    word = input("Введите слово: ")
    result += word + " "

# Вывод объединенной строки
print("Результат:", result.strip())